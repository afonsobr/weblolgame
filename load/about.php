    <dt>Energia</dt>
    <dd>
        Você gasta energia sempre que for jogar alguma partida (normal ou ranked). A cada 4 minutos, você recupera 1 de Energia, ou então, você pode recuperar sua energia por 1,500 IP na Loja.
    </dd>
    <dt>Coaching/EloJob/EloBoost</dt>
    <dd>
        Essas 3 coisas são diferentes. EloJob e Coaching são comprados com R$, enquanto o EloBoost é comprado com RP.
        <ul>
            <li>EloJob: Você compra PdLs, que servem para colocar você em um Tier maior.</li>
            <li>Coaching: Serve para aumentar os Stats que você ganha em uma partida, seja ela Normal ou Ranked.</li>
            <li>EloBoost: Aumenta a taxa de PdLs ganho nas partidas Ranqueadas, e portanto, só é gasto nas Rankeds.</li>
        </ul>
    </dd>
    <dt>Normais</dt>
    <dd>
        As normais servem basicamente para ganhar stats (que servirão nas Rankeds). Basicamente, para se vencer uma partida Normal é preciso ter Sorte e as taxas normais de Vitória/Derrota (40/60) alteradas pelo nível de Maestria. Para cada nível de Maestria com o Campeão você ganha de 2~5% na taxa de Vitória, e o mesmo vale para o nível de Maestria da sua Rota.<br>
        Então, vamos supor (Nível de Maestria com Campeão / Nível de Maestria na Rota) que estou 0/0, sendo assim, tenho chance de 40% de Vitória.<br>
        Caso eu for 2/3, terei de 2*(2) + 3*(2) + 40 = 50% até 2*(5) + 3*(5) + 40 = 65% de chances de Vitória.<br>
        Sendo assim, sendo 7/7 (máximo), suas chances de Vitória variam de 7*(2) + 7*(2) + 40 = 68% até 7*(5) + 7*(5) + 40 = 110% de chances de Vitória (ou seja, desde 68% até a Vitória garantida).<br>
        Basicamente, isso significa que seu PdM é uma bola de neve: quanto mais você tem, mais chances de ganhar uma Normal Game, mas no início vai ser mais difícil, mas o <i>scaling</i> é magnífico!
    </dd>
    <dt>Ranqueadas</dt>
    <dd>
